-- תקציב כישלונות לתבנית.
--
-- מטא לא מפרסמת סף שבו תבנית "מתה". מדידה על חשבון חי (n=4,998 שליחות, בבקרה על
-- היסטוריית החסימות של הנמענת — שאחרת מתחזה לאפקט של התבנית) מראה הידרדרות רציפה:
--
--   כישלונות שהתבנית צברה │ מסירה
--   ──────────────────────┼───────
--   0-4                   │ 86.3%
--   5-9                   │ 83.1%
--   10-14                 │ 67.0%
--   15-19                 │ 66.7%
--   50-99                 │ 19.6%   ← הקריסה
--   100+                  │ 17.2%
--
-- אין צוק; יש מדרון. 15 הוא בלם זהירות — הוא עוצר את השליחה בתבנית שמידרדרת בעודה
-- רחוקה מהקריסה, ומתריע שצריך תאומה. הספירה עצמה נגזרת מ-drip.sent_messages ולא
-- נשמרת בעמודה, כך שתאומה בשם חדש מתחילה אוטומטית מאפס — בדיוק כמו אצל מטא.
ALTER TABLE drip.compliance
  ADD COLUMN IF NOT EXISTS max_template_failures integer NOT NULL DEFAULT 15;

COMMENT ON COLUMN drip.compliance.max_template_failures IS
  'מספר כישלוני המסירה שאחריו המנוע מפסיק לשלוח בתבנית ומתריע שצריך תאומה. 0 = בלי בלם.';
